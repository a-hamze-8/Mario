void loadlevels()
{
    level1 = (int **)malloc(dimensions[0][0] * sizeof(int *));
    for (int i = 0; i < dimensions[0][0]; i++)
    {
        *(level1 + i) = (int *)malloc(dimensions[0][1] * sizeof(int));
    }

    level2 = (int **)malloc(dimensions[1][0] * sizeof(int *));
    for (int i = 0; i < dimensions[1][0]; i++)
    {
        *(level2 + i) = (int *)malloc(dimensions[1][1] * sizeof(int));
    }

    for (int i = 0; i < dimensions[0][0]; i++)
    {
        for (int j = 0; j < dimensions[0][1]; j++)
        {
            level1[i][j] = map1[i][j];
        }
    }
    for (int i = 0; i < dimensions[1][0]; i++)
    {
        for (int j = 0; j < dimensions[1][1]; j++)
        {
            level2[i][j] = map2[i][j];
        }
    }

    mapsList = (int ***)malloc(NUMBERSOFMAPS * sizeof(int **));
    *mapsList = level1;
    *(mapsList + 1) = level2;

    pipesList[0] = pipesMap1;
    pipesList[1] = pipesMap2;

    mEnemyList[0] = enemiesMap1;
    mEnemyList[1] = enemiesMap2;

    pFlowerList[0] = pFlowersMap1;
    pFlowerList[1] = NULL;

    coinBlockList[0] = NULL;
    coinBlockList[1] = coinBlocksMap2;
}
